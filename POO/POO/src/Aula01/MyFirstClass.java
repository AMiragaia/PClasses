package Aula01;

public class MyFirstClass {

	public static void main(String[] args) {
		System.out.println("Hello Eclipse!"); // print da mensagem "Hello Eclipse!"
		int sum = 0; // declarar variavél sum = 0
		for (int i = 1; i <= 100; i++) { // for loop a começar em 1 até 100 de 1 em 1
			sum += i; // soma de todos os numeros do for loop
		}
		System.out.println(sum); // print da soma
	}
}
